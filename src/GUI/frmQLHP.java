    public void loadData() {
        try {
            Connect a = new Connect();
            Connection conn = a.getConnection();
            int number;
            Vector row, column;
            column = new Vector();
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM HOCPHAN");
            ResultSetMetaData metadata = rs.getMetaData();
            number = metadata.getColumnCount(); //trả về số cột

            for (int i = 1; i <= number; i++) {
                column.add(metadata.getColumnName(i)); //lấy ra tiêu đề của các cột
            }
            tbn.setColumnIdentifiers(column);

            while (rs.next()) {
                row = new Vector();
                for (int i = 1; i <= number; i++) {
                    row.addElement(rs.getString(i));
                }
                tbn.addRow(row);
                tblHP.setModel(tbn);
            }
            tblHP.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
                @Override
                public void valueChanged(ListSelectionEvent e) {
                    if (tblHP.getSelectedRow() >= 0) {
                        txtMaHP.setText(tblHP.getValueAt(tblHP.getSelectedRow(), 0) + "");
                        txtTenHP.setText(tblHP.getValueAt(tblHP.getSelectedRow(), 1) + "");
                        txtSTC.setText(tblHP.getValueAt(tblHP.getSelectedRow(), 2) + "");
                        cbbMaHP.setSelectedItem(tblHP.getValueAt(tblHP.getSelectedRow(), 0) + "");
                    }
                }
            });
        } catch (Exception e) {
            System.out.println(e.toString());
        }
    }

    private void updateComboboxMaHP() {
        try {
            String sql = "SELECT MAHP FROM HOCPHAN GROUP BY MAHP";
            ps = conn.prepareStatement(sql);
            rs = ps.executeQuery();

            //Tạo 1 DefaultComboboxModel
            DefaultComboBoxModel modelMaHP = (DefaultComboBoxModel) cbbMaHP.getModel();
            modelMaHP.removeAllElements(); //Xóa hết dữ liệu trong combobox
            while (rs.next()) {
                String mahp = rs.getString("MAHP");
                cbbMaHP.addItem(mahp);
            }
            rs.close();
        } catch (Exception e) {
            System.out.println(e.toString());
        }
    }

