    public void del() {
        try {
            Connect a = new Connect();
            Connection conn = a.getConnection();
            PreparedStatement comm = conn.prepareStatement("DELETE HOCPHAN WHERE MAHP=?");
            comm.setString(1, tblHP.getValueAt(tblHP.getSelectedRow(), 0).toString());
            if (JOptionPane.showConfirmDialog(this, "BẠN CÓ MUỐN XÓA?", "Confirm", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                comm.executeUpdate();
                tbn.setRowCount(0);
                loadData();
                JOptionPane.showMessageDialog(this, "XOA THÀNH CÔNG");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "XÓA THẤT BẠI");
        }
    }

